/**
  * op_add - func
  * @a: param
  * @b: param
  * Return: int
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - func
  * @a: param
  * @b: param
  * Return: int
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - func
  * @a: param
  * @b: param
  * Return: int
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - func
  * @a: param
  * @b: param
  * Return: int
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - func
  * @a: param
  * @b: param
  * Return: int
  */
int op_mod(int a, int b)
{
	return (a % b);
}
